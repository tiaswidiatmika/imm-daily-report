     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'title' => 'required',
            'body' => 'required',
        ]);
        
        Post::create([
            'user_id' => auth()->user()->id,
            'title' => $validated['title'],
            'body' => $validated['body'],
        ]);
        
        return back();
    }

